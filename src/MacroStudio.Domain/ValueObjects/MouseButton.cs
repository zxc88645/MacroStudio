namespace MacroStudio.Domain.ValueObjects;

/// <summary>
/// Represents the different mouse buttons that can be clicked.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The left mouse button (primary button).
    /// </summary>
    Left,

    /// <summary>
    /// The right mouse button (secondary button).
    /// </summary>
    Right,

    /// <summary>
    /// The middle mouse button (wheel button).
    /// </summary>
    Middle,

    /// <summary>
    /// The first extended mouse button (X1).
    /// </summary>
    XButton1,

    /// <summary>
    /// The second extended mouse button (X2).
    /// </summary>
    XButton2
}